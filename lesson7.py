Celcius = int(input("Masukkan suhu udara (Celcius): "))

reamur = (4/5) * (Celcius)
fahrenheit = ((9/5) * (Celcius) ) + 32
kelvin = Celcius + 273

print(f"suhu Celcius ke reamur adalah {Celcius} C")
print(f"suhu Celcius ke reamur adalah {reamur} R")
print(f"suhu Celcius ke reamur adalah {fahrenheit} F")
print(f"suhu Celcius ke reamur adalah {kelvin} K")